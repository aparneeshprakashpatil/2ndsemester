

public class Cereal {
    String cerealName;
    int cerealPro;

    public Cereal(String name, int amtProtein){
        cerealName = name;
        cerealPro = amtProtein;
    }

    public String getName(){
        return cerealName;
    }

    public int getProtein(){
        return cerealPro;
    }

    
}
